async def crawl_urls(urls: list[str]) -> list[dict]:
    config = CrawlerRunConfig(
        markdown_generator=DefaultMarkdownGenerator()
    )

    async with AsyncWebCrawler() as crawler:
        results = await crawler.arun_many(urls, config=config)

    output = []
    for result in results:
        if result.success:
            output.append({
                "url": result.url,
                "markdown": result.markdown
            })
        else:
            print(f"Failed: {result.url} — {result.error_message}")

    return output


async def search_and_crawl(query: str, max_results: int = 8) -> list[dict]:
    urls = duckduckgo_search(query, max_results)
    print(f"Found {len(urls)} URLs, crawling...")
    return await crawl_urls(urls)

if __name__ == "__main__":
    results = asyncio.run(search_and_crawl("benefits and drawbacks of llama.cpp library"))

