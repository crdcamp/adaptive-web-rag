# %% Imports
import asyncio
from ddgs import DDGS
from crawl4ai import AsyncWebCrawler, CrawlerRunConfig
from crawl4ai.markdown_generation_strategy import DefaultMarkdownGenerator

# %% DuckDuckGo Search Tool
# Find out if there's a chunk splitter designed for crawl4ai results
# Also... there's probably a better way to do this function in Go
def duckduckgo_search(search_query: str, max_results: int) -> list:
    result = DDGS().text(search_query, max_results=max_results)

    # Extract only the hrefs (urls) from the `result` dictionary
    hrefs = [r["href"] for r in result]

    return hrefs

